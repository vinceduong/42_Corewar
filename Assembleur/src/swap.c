#include "asm.h"

int				swap_int(int n)
{
	return (((n & 0x000000ff) << 24)
		| ((n & 0x0000ff00) << 8)
		| ((n & 0x00ff0000) >> 8)
		| ((n & 0xff000000) >> 24));
}

unsigned int	swap_uint(unsigned int n)
{
	return (((n & 0x000000ff) << 24)
		| ((n & 0x0000ff00) << 8)
		| ((n & 0x00ff0000) >> 8)
		| ((n & 0xff000000) >> 24));
}

short			swap_short(short n)
{
	return (((n & 0x00ff) << 8)
		| ((n & 0xff00) >> 8));
}
