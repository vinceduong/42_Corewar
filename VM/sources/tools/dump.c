#include "vm.h"

void	dump_ram(t_vm *vm)
{
	int i;

	i = -1;
	while (++i < MEM_SIZE)
	{
		i % 64 == 0 ? ft_printf("0x%.4x : ", i) : 0;
		ft_printf("%.2x ", vm->ram[i]);
		(i + 1) % 64 == 0 ? ft_putendl("") : 0;
	}
	exit(1);
}
